      <p>The absence of a deep emotional connection between humans and the natural world is at the root of the environmental crisis.

Be honest with yourself.

If you really have a deep connection with this place, it is likely that you might feel moved to do something. That’s obvious, right? We naturally and instinctively act to protect that which we care very deeply about.

You are walking through town at night and you see your best friend looking dishevelled, sleeping rough on the street. You haven’t seen him for six months. There is probably an initial shock: “Oh my God, what happened?!!” You instinctively reach out for him. Before you know it your arm is on his shoulder: “Come home with me. Let me take care of you”.

Now another night. This time you see a random stranger looking dishevelled, sleeping rough on the street. Be honest. What do you do? Is it someone else’s problem? If your heart is big enough you might give him some money or buy him a sandwich but is he coming home to sleep on your couch? Perhaps you rationalise your inaction by reminding yourself that you donate to the local homeless charity. You might remind yourself to vote for a politician who appears committed to doing something. But most of the time most of us don’t do anything at all.</p>
